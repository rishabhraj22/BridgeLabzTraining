class AgeOfHarry{
    public static void main(String[] args) {

        // Birth year of Harry
        int birthYear = 2000;

        // Current year
        int currentYear = 2024;

        // Calculate age
        int age = currentYear - birthYear;

        // Display the result
        System.out.println("Harry's age in 2024 is " + age);
    }
}
